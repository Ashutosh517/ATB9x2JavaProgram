package Oct.ex_11102024_Operators;

public class Lab038 {
    public static void main(String[] args) {
        // Arithmetic Operators
//        - (Subtraction)
//        + (Addition)
//        * (Multiplication)
//        / (Division)
//        % (Modulo)   // know as reminder
         int a = 20;
         int b = 10;
        System.out.println(a+b);
        System.out.println(a-b);
        System.out.println(a*b);
        System.out.println(a/b);
        System.out.println(a%b);

    }
}
