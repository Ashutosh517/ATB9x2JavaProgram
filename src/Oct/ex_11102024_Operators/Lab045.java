package Oct.ex_11102024_Operators;

public class Lab045 {
    public static void main(String[] args) {
        // !,&&,||   // this known as logical operator
        boolean a = true;
        boolean b = true;
        boolean c = true || false; // double pipe symbol (||) is OR Gate
        System.out.println(c);
        System.out.println(!a);// !a -- means reverse of a
        System.out.println(!!a);
        System.out.println(!!!a);
        System.out.println(!b);
        System.out.println(!!b);
    }
}
